import React, { Component } from 'react';
import './App.css';

const Box = props => {
  const boxStyle = {
    width: '100px',
    height: '100px',
    backgroundColor: props.color
  }
  return (<div style={boxStyle} />);
}

class App extends Component {
  
  constructor(props) {
    super(props);
    const boxColors = [];
    for(let i = 0; i < 32; i++){
      boxColors.push(props.allColors[Math.floor(Math.random()*props.allColors.length)])
    }
    this.state = {boxColors}
    setInterval(() => {
      const newBoxColors = [...this.state.boxColors]
      newBoxColors[Math.floor(Math.random()*newBoxColors.length)] = props.allColors[Math.floor(Math.random()*props.allColors.length)]
      this.setState({boxColors: newBoxColors})
    },300)
  }
  
  render() {
    const containerStyle = {
      display: 'flex',
      flexWrap: 'wrap',
      flexDirection: 'row',
    };

    return (
      <div className="App" style={containerStyle}>
        {this.state.boxColors.map(color => <Box color={color}/>)}
      </div>
    );
  }
}

